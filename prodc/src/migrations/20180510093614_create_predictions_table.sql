/*TABLA LA CUAL NOS PERMITE GUARDAR LA INFORMACION DE LAS DISTINTAS PREDICCIONES QUE REALIZA UN USUARIO EN DISTINTAS FECHAS*/

CREATE TABLE predictions(
	id          int(10) auto_increment PRIMARY KEY,	/*ATRIBUTO EL CUAL PERMITE IDENTIFICAR UNIVOCAMENTE UNA PREDICCION*/
	result      int(1),
	user_id     int(10),							/*ATRIBUTO EL CUAL HACE REFERENCIA AL USUARIO QUE REALIZO DICHA PREDICCION, ES DECIR ES FOREIGN KEY DE LA TABLA USERS*/
	game_id 	int(10)								/*ATRIBUTO EL CUAL HACE REFERENCIA AL PARTIDO SOBRE EL CUAL REALIZO LA PREDICCION, ES DECIR ES FOREIGN KEY DE LA TABLA GAMES*/
)ENGINE=InnoDB;